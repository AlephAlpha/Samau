codeToChar :: Integral a => CodePage -> a -> Char
codeToChar = genericIndex

charToCode :: Integral a => CodePage -> Char -> Maybe a
charToCode codePage c = lookup c $ zip codePage [1..]

codeToString :: Integral a => CodePage -> [a] -> String
codeToString codePage = map $ codeToChar codePage

stringToCode :: Integral a => CodePage -> String -> [a]
stringToCode codePage = catMaybes . map (charToCode codePage)

readEncodedFile :: CodePage -> FilePath -> IO String
readEncodedFile codePage = (>>= return . codeToString codePage . B.unpack) . B.readFile

